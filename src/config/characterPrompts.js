// 获取角色的特定提示词
export const getCharacterSpecificPrompt = (characterName) => {
  return characterPrompts.predefined[characterName]?.specificPrompt || '';
};

// 获取角色的技能
export const getCharacterSkills = (characterName) => {
  return characterPrompts.predefined[characterName]?.skills || '';
};

// 检查是否是预定义角色
export const isPredefinedCharacter = (characterName) => {
  return Object.keys(characterPrompts.predefined).includes(characterName);
};

